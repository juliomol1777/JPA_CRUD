    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        if (jTextNombre.getText().equals("") || jTextApellido.getText().equals("") || jTextDNI.getText().equals("")
            || jTextEmail.getText().equals("") || jTextCelular.getText().equals("") || jComboBoxDocument.getSelectedItem() == null 
            || jComboBoxPaises.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Debe rellenar todos los campos");
        } else {
            try {
                tipoDocumento= new Tipodocumentos();
                paises= new Paises();
                String nombre = jTextNombre.getText();
                String apellido = jTextApellido.getText();
                int dni = Integer.parseInt(jTextDNI.getText());
                String email = jTextEmail.getText();
                String celular = jTextCelular.getText();
                String idtipodocumento = (String) jComboBoxDocument.getSelectedItem();
                String idtiponacionalidad = (String) jComboBoxPaises.getSelectedItem();
                tipoDocumento.setTipodocumento(idtipodocumento); 
                paises.setNombre(idtiponacionalidad);
                CTipoDoc.create(tipoDocumento);
                CPaises.create(paises);
                alumno = new Alumnos(nombre, apellido, dni, email, celular, tipoDocumento, paises);
                CAlumnos.create(alumno);
                JOptionPane.showMessageDialog(null, "Registro exitoso");
            } catch (HeadlessException | NumberFormatException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
        llenarTabla();
        vaciarCampos();
    }//GEN-LAST:event_botonAgregarActionPerformed

    private void botonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBorrarActionPerformed
        if (jtabla.getSelectedRow() >= 0) {
            try {
                CAlumnos.destroy(alumnos.get(jtabla.getSelectedRow()).getIdalumno());
                JOptionPane.showMessageDialog(null, "Registro eliminado con exito");
            } catch (NonexistentEntityException | HeadlessException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila de la tabla");
        }

        vaciarCampos();
        llenarTabla();
    }//GEN-LAST:event_botonBorrarActionPerformed

    private void botonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActualizarActionPerformed
        if (jTextNombre.getText().equals("") || jTextApellido.getText().equals("") || jTextDNI.getText().equals("")
                || jTextEmail.getText().equals("") || jTextCelular.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe rellenar todos los campos");
        } else {
            try {
                alumnoEdit= new Alumnos(Integer.parseInt(jtabla.getValueAt(jtabla.getSelectedRow(), 0).toString()));
                alumnoEdit.setIdtipodocumento((Tipodocumentos) jComboBoxDocument.getSelectedItem());
                alumnoEdit.setIdtiponacionalidad((Paises) jComboBoxPaises.getSelectedItem());
                alumnoEdit.setNombre(jTextNombre.getText());
                alumnoEdit.setApellido(jTextApellido.getText());
                alumnoEdit.setDni(Integer.parseInt(jTextDNI.getText()));
                alumnoEdit.setEmail(jTextEmail.getText());
                alumnoEdit.setCelular(jTextCelular.getText());
                CAlumnos.edit(alumnoEdit);
                JOptionPane.showMessageDialog(null, "Se editó el registro");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
        vaciarCampos();
        llenarTabla();
    }//GEN-LAST:event_botonActualizarActionPerformed

