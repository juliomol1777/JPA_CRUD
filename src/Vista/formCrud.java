    public void llenarPaises(){
        em = emf.createEntityManager();
        Iterator it = em.createNamedQuery("Paises.findAll").getResultList().iterator();
        while(it.hasNext()){
            this.jComboBoxPaises.addItem((Paises) it.next());
        }
    }
    public void llenarTipoDoc(){
        em = emf.createEntityManager();
        Iterator it = em.createNamedQuery("Tipodocumentos.findAll").getResultList().iterator();
        while(it.hasNext()){
            this.jComboBoxDocument.addItem((Tipodocumentos) it.next());
        }
    }
    public void llenarTabla(){
        String [] nombres= {"ID", "Nombre", "Apellido", "DNI", "Email", "Celular", "Tipo de Documento", "Pais"};
        Object [] datos=null;
        DefaultTableModel dtm= new DefaultTableModel(null, nombres);
        alumnos= CAlumnos.findAlumnosEntities();
        for (int i = 0; i < alumnos.size(); i++) {
            dtm.addRow(datos);
            dtm.setValueAt(alumnos.get(i).getIdalumno(), i,0);
            dtm.setValueAt(alumnos.get(i).getNombre(), i,1);
            dtm.setValueAt(alumnos.get(i).getApellido(), i,2);
            dtm.setValueAt(alumnos.get(i).getDni(), i,3);
            dtm.setValueAt(alumnos.get(i).getEmail(), i,4);
            dtm.setValueAt(alumnos.get(i).getCelular(), i,5);
            dtm.setValueAt(alumnos.get(i).getIdtipodocumento().getTipodocumento(), i,6);
            dtm.setValueAt(alumnos.get(i).getIdtiponacionalidad().getNombre(), i,7);
            }
        jtabla.setModel(dtm);
    }
